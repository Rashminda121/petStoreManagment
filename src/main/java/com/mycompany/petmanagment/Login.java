    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        if(username.getText().isEmpty()||password.getText().isEmpty()){
           
            JOptionPane.showMessageDialog(this, "Missing Values.");
        }else{
            
            try {
            conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/petshopdb","root","1234");
            st=conn.createStatement();
            
            String sql="Select * from user where username='"+username.getText()+"' and upass='"+password.getText()+"'";
            rs = st.executeQuery(sql);
            
            
            if(rs.next()){
                
                new pets().setVisible(true);
                this.dispose();
            
            }else{
                JOptionPane.showMessageDialog(this, "Wrong Username or Password");
                username.setText("");
                password.setText("");
            }
            conn.close();
           
            
        } catch (SQLException ex) {
            
            ex.printStackTrace();
        }
       
            
        }
    }//GEN-LAST:event_loginActionPerformed

